$(document).ready(()=>{
    $("#registration").validate({
        rules:{
            name:{
                required:true,
                minlength:4,
            },
            email:{
                required:true,
                email:true,
            },
            phone_number:{
                required:true,
                matches: "[0-9]+",  // <-- no such method called "matches"!
                minlength:10,
                maxlength:10,
            },
            password:{
                required:true,
                minlength:8,
            },   
            password2:{
                required:true,
                minlength:8,
            },
        }
        })
    })